Drop Database HRMS;

Create Database HRMS;

CREATE TABLE HRMS.PROJECT (
    ID int NOT NULL AUTO_INCREMENT,
    Project_Name varchar(255) NOT NULL,
    PRIMARY KEY (ID)
)ENGINE=InnoDB;

CREATE TABLE HRMS.EMPLOYEE (
    ID int NOT NULL AUTO_INCREMENT,
    First_Name varchar(255) NOT NULL,
    Last_Name varchar(255) NOT NULL,
    User_Name varchar(255) UNIQUE NOT NULL,
    Emp_ID varchar(255) UNIQUE NOT NULL,
    Password varchar(255) NOT NULL,
    Designation varchar(255),
    DOJ DATE,
    Salt varchar(255) NOT NULL,
    Project_Id int,
    PRIMARY KEY (ID),
    KEY `EMPLOYEE_fk_1` (`Project_Id`),
    CONSTRAINT `EMPLOYEE_fk_1` FOREIGN KEY (`Project_Id`) 
 	REFERENCES `Project` (`ID`)
)ENGINE=InnoDB;

CREATE TABLE HRMS.Firm_Designations (
    ID int NOT NULL AUTO_INCREMENT,
    Designation varchar(255) NOT NULL,
    Designation_Seniority int NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE HRMS.Subordinates (
    ID int NOT NULL AUTO_INCREMENT,
    Sub_ID varchar(255) NOT NULL,
    Emp_Id varchar(255) NOT NULL,
    KEY `Subordinates_fk_1` (`Emp_Id`),
    PRIMARY KEY (ID),
 	CONSTRAINT `Subordinates_fk_1` FOREIGN KEY (`Emp_Id`) 
 	REFERENCES `EMPLOYEE` (`Emp_Id`)
)ENGINE=InnoDB;

CREATE TABLE HRMS.Superiors (
    ID int NOT NULL AUTO_INCREMENT,
    Sup_ID varchar(255) NOT NULL,
    Emp_ID varchar(255) NOT NULL,
    KEY `Superiors_fk_1` (`Emp_Id`),
    PRIMARY KEY (ID),
 	CONSTRAINT `Superiors_fk_1` FOREIGN KEY (`Emp_Id`) 
 	REFERENCES `EMPLOYEE` (`Emp_ID`)
)ENGINE=InnoDB;

CREATE TABLE HRMS.Leave_Table (
    ID int NOT NULL AUTO_INCREMENT,
    User_Name varchar(255) NOT NULL,
    Emp_ID varchar(255) NOT NULL,
    Leave_Date DATE NOT NULL,
    Leave_Status varchar(255),
    Reason varchar(255) NOT NULL,
    Superior_Emp_ID varchar(255) NOT NULL,
    Supervisor_Comment varchar(255),
    PRIMARY KEY (ID)
);

CREATE TABLE HRMS.Punch_Record (
    ID int NOT NULL AUTO_INCREMENT,
    User_Name varchar(255) NOT NULL,
    Emp_ID varchar(255) NOT NULL,
    Punch_Date DATE NOT NULL,
    PRIMARY KEY (ID)
)ENGINE=InnoDB;

CREATE TABLE HRMS.Punch_Time (
    ID int NOT NULL AUTO_INCREMENT,
    Punch_Record_Id int NOT NULL,
    P_Time TIME NOT NULL,
    PRIMARY KEY (ID),
    KEY `punch_Time_fk_1` (`Punch_Record_Id`),
 	CONSTRAINT `punch_Time_fk_1` FOREIGN KEY (`Punch_Record_Id`) 
 	REFERENCES `Punch_Record` (`ID`) ON DELETE CASCADE ON UPDATE CASCADE
)ENGINE=InnoDB;

CREATE TABLE HRMS.Attendance (
    ID int NOT NULL AUTO_INCREMENT,
    User_Name varchar(255) NOT NULL,
    Emp_ID varchar(255) NOT NULL,
    Attendance varchar(255) NOT NULL,
    Attendance_Date DATE NOT NULL,
    Work_Hours decimal NOT NULL,
    Work_Hours_String varchar(255),
    PRIMARY KEY (ID)
)ENGINE=InnoDB;


CREATE TABLE HRMS.CUSTOMER (
    ID int NOT NULL AUTO_INCREMENT,
    First_Name varchar(255) NOT NULL,
    Last_Name varchar(255) NOT NULL,
    User_Name varchar(255) UNIQUE NOT NULL,
    Password varchar(255) NOT NULL,
    Salt varchar(255) NOT NULL,
    PRIMARY KEY (ID)
)ENGINE=InnoDB;

